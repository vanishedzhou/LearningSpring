package com.zzy.beanInherit;

public class InheritProduct {
	private String inheritedAttr;
	private String name;
	private String price;
	private int nums;
	
	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public int getNums() {
		return nums;
	}

	public void setNums(int nums) {
		this.nums = nums;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getInheritedAttr() {
		return inheritedAttr;
	}

	public void setInheritedAttr(String inheritedAttr) {
		this.inheritedAttr = inheritedAttr;
	}
	
	

}
